/**
 * AI 티 제거 — 금지 표현 목록
 * 매번 랜덤으로 4개를 뽑아 프롬프트에 삽입
 */

const ALL_ANTI_PATTERNS = [
  '"~에 대해 알아보겠습니다" / "~에 대해 살펴보겠습니다"',
  '"마무리하며" / "마치며" / "글을 마무리하면서"',
  '"다양한" / "풍부한" / "중요한" 같은 의미 없는 수식어 반복',
  '"자, 그럼 시작해볼까요?" 같은 인위적 전환',
  '"~라고 할 수 있습니다" / "~것으로 보입니다" 같은 논문체',
  '"오늘은 ~ 주제로 이야기해보려고 합니다" 같은 서론',
  '"여러분" 반복 호칭 (한 글에 2번 이상)',
  '"~하는 것이 좋습니다" 반복 (직접적 표현으로 대체)',
  '"이처럼" / "이렇듯" / "이와 같이" 같은 접속어 남용',
  '"흥미롭게도" / "놀랍게도" / "재미있게도" 반복',
  '"~하는 것은 물론이고" 같은 기계적 나열 표현',
  '"지금까지 ~에 대해 알아보았습니다" 같은 요약 맺음',
];

/** 금지 표현 중 count개를 랜덤으로 뽑아 반환 */
export function pickAntiPatterns(count = 4): string[] {
  const shuffled = [...ALL_ANTI_PATTERNS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}
