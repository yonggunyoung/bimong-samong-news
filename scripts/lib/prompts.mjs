/**
 * 카테고리 + 스타일에 맞는 프롬프트 조합
 * @param {string} category
 * @param {object} style - STYLES 배열 요소
 * @returns {string}
 */
export function buildPrompt(category, style) {
  return `당신은 "비몽사몽" 채널의 글을 쓰는 에디터입니다.
비몽사몽은 꿈과 일상, 그 사이 어딘가에 있는 이야기를 다루는 채널입니다.

${COMMON_RULES}

${CATEGORY_EXPERTISE[category] ?? ""}

${style.instruction}

${RESPONSE_FORMAT}`;
}

/**
 * AI로 새로운 주제를 동적 생성하는 프롬프트
 * @param {string} category
 * @param {string[]} existingTopics - 기존 제목 목록 (중복 방지)
 * @returns {string}
 */
export function buildTopicGenerationPrompt(category, existingTopics) {
  const recent = existingTopics.slice(0, 30).join("\n- ");
  return `당신은 "비몽사몽" 블로그의 편집장입니다.

카테고리: ${category}

기존에 발행된 주제 (중복 금지):
- ${recent}

위 주제와 겹치지 않는 새로운 기사 주제 1개를 제안해주세요.

조건:
- 한국어 독자 대상
- 검색 유입이 될 만한 키워드 포함
- 현재 시기/계절에 맞는 주제 우선
- 호기심을 자극하는 제목 형식

반드시 아래 JSON 형식으로만 응답하세요:
{"topic": "주제 제목"}`;
}

/**
 * 랜덤 스타일 선택
 */
export function pickRandomStyle() {
  return STYLES[Math.floor(Math.random() * STYLES.length)];
}
